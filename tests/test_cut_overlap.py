"""Дедуп шва между чанками.

Все склейки взяты из настоящих стенограмм (2026-07-22_1626_live.md и др.):
STT распознаёт перекрытие соседних чанков по-разному, и текст двоился.
Живая речь в контрольной части — тоже реальные конструкции: человек часто
начинает две подряд мысли одинаково, и резать это нельзя.
"""
import pathlib
import sys

import pytest

sys.path.insert(0, str(pathlib.Path(__file__).resolve().parent.parent / "src"))

from main import Transcript  # noqa: E402

cut = Transcript._cut_overlap


@pytest.mark.parametrize("prev,new,expected", [
    # хвост предыдущего чанка повторён в начале нового, начало расслышано иначе
    ("Поздравляю, вдруг они прям с ходу такие залетят, как бы.",
     "А вдруг они прям с ходу такие залетят, как бы? Вообще, на самом деле, знаете",
     "Вообще, на самом деле, знаете"),
    ("Вот тут, на самом деле, не так-то просто, потому что",
     "Тут, на самом деле, не так-то просто, потому что параметры подключения",
     "параметры подключения"),
    # почти весь чанк повторён, прирост в хвосте
    ("Я сейчас посмотрю, как я в пайтоне делал. Я, по-моему, вообще в пайтоне там как",
     "Сейчас посмотрю, как я в пайтоне делал. Я, по-моему, вообще в пайтоне там какая-то хитрая",
     "какая-то хитрая"),
    # расхождение в середине рвёт совпадение надвое
    ("Если завтра получится, будет, то очень хорошо.",
     "Если завтра получится, будет хорошо. Понятно.",
     "Понятно."),
])
def test_seam_is_cut(prev, new, expected):
    assert cut(prev, new).strip(" .") == expected.strip(" .")


@pytest.mark.parametrize("prev,new", [
    # STT дописал спереди «А вот» — прироста нет
    ("В руководстве пользователя вроде нету адреса.",
     "А вот в руководстве пользователя вроде нету адреса."),
    # разошлись только в последнем слове: концовку расслышали иначе
    ("Ну, тебе только надо сопоставить соответствующий запрос в магине.",
     "Ну, тебе только надо сопоставить соответствующий запрос в кабинете."),
])
def test_full_duplicate_gives_nothing(prev, new):
    assert cut(prev, new) == ""


@pytest.mark.parametrize("prev,new", [
    ("Да.", "Да."),                       # короткое — не трогаем
    ("Ну хорошо.", "Тогда идём дальше, обсудим следующий пункт"),
    ("Мы обсудили сроки по задаче.", "Теперь давайте про бюджет на квартал"),
    ("Понятно.", "Понятно, тогда так и сделаем"),
    ("Он сказал, что сроки горят.", "Она ответила, что успеет к пятнице"),
    ("Задача 3318 в работе.", "Задача 3278 ещё не начата."),
    # две разные мысли с общим началом — совпадение 0.75, порог выше
    ("Нужно посмотреть на бюджет.", "Нужно посмотреть на сроки."),
    ("Давайте начнём.", "Давайте закончим."),
    ("Это первый вопрос.", "Это второй вопрос."),
    ("Я про это и говорю.", "Вот именно об этом я и говорил на прошлой встрече"),
])
def test_live_speech_survives(prev, new):
    assert cut(prev, new) == new


def test_repeat_in_the_middle_is_not_a_seam():
    """Совпадение далеко от начала нового чанка — обычный повтор внутри речи.

    Два служебных слова спереди («а вот») дедуп прощает: так выглядит реальная
    склейка. Дальше второго слова — уже не шов, а человек вернулся к теме.
    """
    prev = "Мы обсуждали это на прошлой неделе"
    new = "Да, и ещё, кстати, помнишь, мы обсуждали это на прошлой неделе"
    assert cut(prev, new) == new


@pytest.mark.parametrize("prev,new", [("", "Что-то сказали"), ("Что-то сказали", "")])
def test_empty_input_is_safe(prev, new):
    assert cut(prev, new) == new


def test_exact_short_tail_still_works():
    """Прежнее поведение: точное совпадение хвоста режется от двух слов.

    Одно слово не режем — «по» в конце и «по» в начале сплошь и рядом
    оказываются разными предлогами.
    """
    assert cut("идём дальше по списку", "по списку задач на август") == "задач на август"
    assert cut("идём дальше по", "по списку задач") == "по списку задач"
